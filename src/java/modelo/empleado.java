package modelo;

/**
 *
 * @author andres
 */
public class empleado {
    
    private String nombre, codigo, contrasena;

    public empleado() {
    }

    public empleado(String nombre, String codigo, String contrasena) {
        this.nombre = nombre;
        this.codigo = codigo;
        this.contrasena = contrasena;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getContrasena() {
        return contrasena;
    }

    public void setContrasena(String contrasena) {
        this.contrasena = contrasena;
    }
    
    
    
    
    
}
